        private static BigInteger MultPow(BigInteger first, BigInteger second, BigInteger modulo)
        {
            BigInteger result;
            // Перевіряємо, який множник більший, щоб зменшити кількість ітерацій
            if (first < second)
            {
                result = first;
                first = second;
                second = result;
            }
            result = 0;
            while (first > 0 && second > 0)
            {
                if ((second & 1) != 0)
                    result ^= first;
                if (first >= BigInteger.Pow(2, length - 1))
                    first = (first << 1) ^ modulo;
                else
                    first <<= 1;
                second >>= 1;
            }
            return result;
        }
        public static BigInteger ModPow(BigInteger w, BigInteger k, BigInteger f)
        {
            BigInteger res = 0, helper = 1, buffer = w;
            BigInteger n = 1;
            while (k > 0)
            {
                n <<= 1;
                if (n > k)
                {
                    n >>= 1;
                    k -= n;
                    n = 1;
                    helper = MultPow(w, helper, f);
                    w = buffer;
                    n <<= 1;
                }
                if (k == 1)
                {
                    res = MultPow(w, helper, f);
                    w = res;
                    k--;
                    break;
                }
                res = MultPow(w, w, f);
                w = res;
            }
            return res;
        }
        public static string ToBinaryString(this BigInteger bigint)
        {
            // Беремо байти числа
            var bytes = bigint.ToByteArray();
            var byteI = bytes.Length - 1;
            // Створюємо базу для строку, що будемо повертати
            var bitString = new StringBuilder(bytes.Length * 8);
            // Конвертуємо молодший байт у BIN
            var binary = Convert.ToString(bytes[byteI], 2);
            bitString.Append(binary);
            // Конвертуємо інші байти
            for (byteI--; byteI >= 0; byteI--)
            {
                bitString.Append(Convert.ToString(bytes[byteI], 2).PadLeft(8, '0'));
            }
            return bitString.ToString();
        }

        public static BigInteger BinToDec(string bitString)
        {
            BigInteger integer = 0;
            foreach (char bit in bitString)
            {
                integer <<= 1;
                integer += bit == '1' ? 1 : 0;
            }
            return integer;
        }
